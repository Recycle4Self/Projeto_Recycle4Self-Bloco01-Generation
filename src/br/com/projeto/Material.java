package br.com.projeto;

public class Material {

	private int cadPessoa;
	private String material;
	private int tipoMaterial;
	private double qtdMaterial;


	public int getCadPessoa() {
		return cadPessoa;
	}

	public void setCadPessoa(int cadPessoa) {
		this.cadPessoa = cadPessoa;
	}

	public String getMaterial() {
		return material;
	}

	public void setMaterial(String material) {
		this.material = material;
	}

	public int getTipoMaterial() {
		return tipoMaterial;
	}

	public void setTipoMaterial(int tipoMaterial) {
		this.tipoMaterial = tipoMaterial;
	}

	public double getQtdMaterial() {
		return qtdMaterial;
	}

	public void setQtdMaterial(double qtdMaterial) {
		this.qtdMaterial = qtdMaterial;
	}
}
